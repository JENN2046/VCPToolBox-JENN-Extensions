async function runWorkflow(runtime, workflow, input, apply) {
  const started = Date.now();
  const response = await runtime.handleRequest({
    action: 'run_workflow',
    workflow,
    input,
    apply,
    yes: true,
    format: 'json'
  });

  return {
    workflow,
    apply,
    status: response.status,
    durationMs: Date.now() - started,
    checkpoint: response.result ? response.result.checkpoint : null,
    failed_step: response.result ? response.result.failed_step || null : null,
    error: response.error || null
  };
}

async function main() {
  const pluginBase = path.resolve(__dirname, '..');
  const reportDir = path.resolve(pluginBase, '..', '..', 'docs', 'dingtalk-cli', 'reports');
  fs.mkdirSync(reportDir, { recursive: true });

  const runtime = new DingTalkCLIRuntime({ basePath: pluginBase });
  const inputs = buildInputs();

  const report = {
    generatedAt: nowIso(),
    runs: []
  };

  for (const workflow of Object.keys(inputs)) {
    report.runs.push(await runWorkflow(runtime, workflow, inputs[workflow], false));
    report.runs.push(await runWorkflow(runtime, workflow, inputs[workflow], true));
  }

  const file = path.join(reportDir, `workflow-e2e-${stamp()}.json`);
  fs.writeFileSync(file, JSON.stringify(report, null, 2), 'utf8');
  fs.writeFileSync(path.join(reportDir, 'workflow-e2e-latest.json'), JSON.stringify(report, null, 2), 'utf8');

  const summary = [];
  summary.push(`# DWS Workflow E2E (${report.generatedAt})`);
  summary.push('');
  for (const row of report.runs) {
    summary.push(`- ${row.workflow} apply=${row.apply}: ${row.status}${row.failed_step ? ` failed_step=${row.failed_step}` : ''}`);
  }
  fs.writeFileSync(path.join(reportDir, 'workflow-e2e-latest.md'), `${summary.join('\n')}\n`, 'utf8');

  process.stdout.write(`workflow e2e report generated: ${file}\n`);
}

main().catch((error) => {
  process.stderr.write(`${error.stack || error.message}\n`);
  process.exitCode = 1;
});
